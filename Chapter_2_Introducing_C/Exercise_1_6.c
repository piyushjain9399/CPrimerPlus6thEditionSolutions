/* Exercise_1_6.c -- Integer toes, twice toes, toes squared.  */

#include<stdio.h>
int main(){
    int toes, twiceToes, toesSquared;
    toes = 10;
    twiceToes = 2*toes;
    toesSquared = toes*toes;
    printf("toes: %d, twice toes: %d and toes-squared: %d\n", toes, twiceToes, toesSquared);
    return 0;
}
